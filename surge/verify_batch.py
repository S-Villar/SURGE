def main(batch_dir: str, verbose: bool = False) -> int:
    """Main verification function."""
    batch_dir = os.path.abspath(batch_dir)
    
    if not os.path.isdir(batch_dir):
        print(f"[ERROR] Batch directory does not exist: {batch_dir}")
        return 1
    
    # Load metadata
    try:
        meta = load_meta_json(batch_dir)
    except FileNotFoundError as e:
        print(f"[ERROR] {e}")
        return 1
    
    # Determine mode
    mode = meta.get("mode", "unknown")
    equilibria_mode = meta.get("equilibria_mode", None)
    
    print(f"Verifying batch: {batch_dir}")
    print(f"Mode: {mode}, Equilibria mode: {equilibria_mode}")
    print()
    
    # Route to appropriate verification function
    if mode == "equilibria":
        if equilibria_mode == "set":
            errors, warnings = verify_set_mode_batch(batch_dir, meta, verbose)
        elif equilibria_mode == "fixed":
            errors, warnings = verify_fixed_mode_batch(batch_dir, meta, verbose)
        elif equilibria_mode == "per_case":
            errors, warnings = verify_per_case_mode_batch(batch_dir, meta, verbose)
        else:
            print(f"[ERROR] Unknown equilibria_mode: {equilibria_mode}")
            return 1
    else:
        errors, warnings = verify_simple_batch(batch_dir, meta, verbose)
    
    # Summary
    print()
    print("=" * 60)
    print("Verification Summary:")
    print(f"  Errors: {errors}")
    print(f"  Warnings: {warnings}")
    
    if errors == 0 and warnings == 0:
        print("  Status: ✓ All checks passed")
        return 0
    elif errors == 0:
        print("  Status: ⚠ Passed with warnings")
        return 0
    else:
        print("  Status: ✗ Failed")
        return 1


